<p>I love running. It's an easy activity to get started in, hard to master, and is full of ways to get better. I'm one of those data-obsessed runners who keeps track of way too much. In every skill I learn, I inhale the knowledge around me from experts I know, experts who have books and blogs, and anywhere else I can learn from. Running is no different.</p>
<p>My training philosophy is based on non-linear periodization and I think a minimalist approach makes the most sense. Let me explain.</p>
<p><b>Periodization</b> is based on Arthur Lydiard's training philosophies, where his runners had great success in blocks of training that focus on one aspect of running: building a strong base, gaining strength, or focusing on race page. Lydiard believed that this structure needs to follow a certain order to be extremely successful. It's best when you have one goal race that you can really plan for. <b>Non-linear periodization</b> is a derivative of this format. Its lead philosophy is that one can train all aspects of running at once, but still focusing each period towards a certain skill. It's an intriguing philosophy for people who run a lot of races throughout the season or those who are bored with a single type of training.</p>
<p>There are generally two philosophies on the number of miles run: either you try to run the most miles your body can take, or you try to run the least miles required to achieve your goals. The former is <b>maximalism</b> and the latter is <b>minimalism.</b> I believe you should run as few miles as you need to to achieve your goals because bringing your body closer to breaking doesn't lead to injury-free running.</p>
<p>Finally, I believe that runners need to do strength training, especially in the core and hips. This is an important part of developing a strong base for running.</p>
